def save_checkpoint(state, is_best, filename):
    iter_str = str(state['iter'])
    acc1 = str(round(state['acc1'].item(),4))
    kept_ratio = str(round(state['kept_ratio'],4))
    fullpathname = filename+iter_str+'_'+acc1+'_'+kept_ratio+'_checkpoint.pth.tar'
    bestfilename = '_model_best.pth.tar'
    torch.save(state, fullpathname)
    if is_best:
        shutil.copyfile(fullpathname, bestfilename)
def save_finetune_checkpoint(state, is_best, filename):
    fullpathname = filename+str(state['epoch'])+ \
                   '_'+str(round(state['acc1'].item(),4))+ \
                   '_finetune_checkpoint.pth.tar'
    bestfilename = filename+'_finetune_model_best.pth.tar'
    torch.save(state, fullpathname)
    if is_best:
        shutil.copyfile(fullpathname, bestfilename)
class AverageMeter(object):
    """Computes and stores the average and current value"""
    def __init__(self, name, fmt=':f'):
        self.name = name
        self.fmt = fmt
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count

    def __str__(self):
        fmtstr = '{name} {val' + self.fmt + '} ({avg' + self.fmt + '})'
        return fmtstr.format(**self.__dict__)


class ProgressMeter(object):
    def __init__(self, num_batches, meters, prefix=""):
        self.batch_fmtstr = self._get_batch_fmtstr(num_batches)
        self.meters = meters
        self.prefix = prefix

    def display(self, batch):
        entries = [self.prefix + self.batch_fmtstr.format(batch)]
        entries += [str(meter) for meter in self.meters]
        print('\t'.join(entries))

    def _get_batch_fmtstr(self, num_batches):
        num_digits = len(str(num_batches // 1))
        fmt = '{:' + str(num_digits) + 'd}'
        return '[' + fmt + '/' + fmt.format(num_batches) + ']'


def adjust_learning_rate(optimizer, epoch, args):
    """Sets the learning rate to the initial LR decayed by 10 every 30 epochs"""
    lr = args.lr * (0.1 ** (epoch // 20))
    for param_group in optimizer.param_groups:
        param_group['lr'] = lr


def accuracy(output, target, topk=(1,)):
    """Computes the accuracy over the k top predictions for the specified values of k"""
    with torch.no_grad():
        maxk = max(topk)
        batch_size = target.size(0)

        _, pred = output.topk(maxk, 1, True, True)
        pred = pred.t()
        correct = pred.eq(target.view(1, -1).expand_as(pred))

        res = []
        for k in topk:
            correct_k = correct[:k].view(-1).float().sum(0, keepdim=True)
            res.append(correct_k.mul_(100.0 / batch_size))
        return res
